package homeworkweek6;

/**
 * . Write a Java program to print the sum (addition), multiply, subtract, divide and
 * remainder of two numbers
 */
public class Project18 {
    public static void main(String[] args) {   // print statement
        int a = 125;
        int b = 24;
        System.out.println("125 + 24 = " + (a+b)); // addition
        System.out.println("125 - 24 = " + (a-b)); // subtraction
        System.out.println("125 x 24 = " + a*b); // multiplication
        System.out.println("125 / 24 = " + a/b); // division
        System.out.println("125 mod 24 = 5");
    }
}
